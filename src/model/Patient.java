package model;

/**
 *
 * @author karthiksonti
 */
public class Patient {
    
    private String patientName;
    private String patientID;
    private int patientAge;
    private String patientCity;
    private String patientCommunity;
    private String patientSex;
    private String patientHouse;

    public String getPatientName() {
        return patientName;
    }

    public void setPatientName(String patientName) {
        this.patientName = patientName;
    }

    public String getPatientID() {
        return patientID;
    }

    public void setPatientID(String patientID) {
        this.patientID = patientID;
    }

    public int getPatientAge() {
        return patientAge;
    }

    public void setPatientAge(int patientAge) {
        this.patientAge = patientAge;
    }

    public String getPatientCity() {
        return patientCity;
    }

    public void setPatientCity(String patientCity) {
        this.patientCity = patientCity;
    }

    public String getPatientCommunity() {
        return patientCommunity;
    }

    public void setPatientCommunity(String patientCommunity) {
        this.patientCommunity = patientCommunity;
    }

    public String getPatientSex() {
        return patientSex;
    }

    public void setPatientSex(String patientSex) {
        this.patientSex = patientSex;
    }

    public String getPatientHouse() {
        return patientHouse;
    }

    public void setPatientHouse(String patientHouse) {
        this.patientHouse = patientHouse;
    }
    
    
}
